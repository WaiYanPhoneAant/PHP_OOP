polimorphism aim to implements to get multi forms of data or process from implementing one interface.


Polymorphism allows different classes to implement the same interface or
method signature while providing their own specific implementations. 
This enables these classes to take on multiple forms (hence the term "polymorphism"),
allowing them to be used interchangeably through a common interface. 
This concept is fundamental for creating more flexible and extensible software systems.